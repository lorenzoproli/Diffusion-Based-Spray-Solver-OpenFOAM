"""
Global post-processing for Lambert J20 DBM-kernel sensitivity.

Purpose
-------
Compare, in the same figures:
    - Experimental data
    - Fluent/Lambert reference data
    - OpenFOAM kernel cases: PCM, Dinj/6, Dinj/3, Dinj, 3Dinj, 6Dinj

Expected folder structure
-------------------------
Run this script from:
    ~/runs/subsonic/Lambert_case_J20_kernelStudy

or place it in:
    ~/runs/subsonic/Lambert_case_J20_kernelStudy/postProcessing_kernelComparison/

Expected input profiles, already generated by the single-case post-processing:
    b_Dinj/postProcessing_Lambert_OpenFOAM/profile_OpenFOAM_30d.csv
    b_Dinj/postProcessing_Lambert_OpenFOAM/profile_OpenFOAM_60d.csv
    ... same for all kernel cases

Reference data
--------------
This script tries to import the reference datasets from the single-case
script, if it is available in the same folder:
    postProcessing_kernelComparison/postprocess_lambert_openfoam.py

The preferred format is a dictionary named REF:
    REF["30d"]["fluent_smd"] -> numpy array with columns [y_mm, D32_um]
    REF["30d"]["exp_smd"]    -> numpy array with columns [y_mm, D32_um]
    REF["30d"]["fluent_ux"]  -> numpy array with columns [y_mm, Ux_mps]
    REF["30d"]["exp_ux"]     -> numpy array with columns [y_mm, Ux_mps]

and same for "60d".

If REF is not found, the script still plots the OpenFOAM kernel cases and writes
a warning. In that case, add/copy the old single-case script next to this file.
"""

