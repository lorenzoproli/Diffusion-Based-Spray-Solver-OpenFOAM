def infer_paths(script_path: Path, root_arg: Optional[str]) -> Tuple[Path, Path]:
    """Return (root_case_dir, output_dir)."""
    if root_arg is not None:
        root = Path(root_arg).expanduser().resolve()
        out_dir = root / "postProcessing_kernelComparison"
    else:
        if script_path.parent.name == "postProcessing_kernelComparison":
            out_dir = script_path.parent
            root = out_dir.parent
        else:
            root = Path.cwd().resolve()
            out_dir = root / "postProcessing_kernelComparison"

    out_dir.mkdir(parents=True, exist_ok=True)
    return root, out_dir


